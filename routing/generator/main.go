func readServicesYAML(path string) []string {
	data, err := os.ReadFile(path)
	if err != nil {
		return nil
	}
	return parseServicesYAML(string(data))
}

func parseServicesYAML(content string) []string {
	var imports []string
	lines := strings.Split(content, "\n")
	inImports := false
	for _, line := range lines {
		trimmed := strings.TrimSpace(line)
		if trimmed == "imports:" {
			inImports = true
			continue
		}
		if inImports && strings.HasPrefix(trimmed, "- ") {
			imp := strings.TrimPrefix(trimmed, "- ")
			imports = append(imports, imp)
		}
		if inImports && !strings.HasPrefix(trimmed, "- ") && trimmed != "" {
			inImports = false
		}
	}
	return imports
}

func writeGo(path string, src []byte) {
	formatted, err := format.Source(src)
	if err != nil {
		fmt.Fprintf(os.Stderr, "format error in %s: %v\n", path, err)
		os.WriteFile(path, src, 0644)
		return
	}
	os.WriteFile(path, formatted, 0644)
}

func convertPath(path string) string {
	path = strings.ReplaceAll(path, "{", "(?P<")
	path = strings.ReplaceAll(path, "}", ">[^/]+)")
	return "^" + strings.Trim(path, "/") + "/?$"
}

func formatSlice(items []string) string {
	var quoted []string
	for _, p := range items {
		quoted = append(quoted, fmt.Sprintf("%q", p))
	}
	return "[]string{" + strings.Join(quoted, ", ") + "}"
}

func formatRoles(roles string) string {
	parts := strings.Split(roles, ",")
	var quoted []string
	for _, p := range parts {
		p = strings.TrimSpace(p)
		p = strings.Trim(p, `"`)
		p = strings.TrimSpace(p)
		if p != "" {
			quoted = append(quoted, fmt.Sprintf("%q", p))
		}
	}
	return strings.Join(quoted, ", ")
}

func formatMethods(methods string) string {
	parts := strings.Split(methods, ",")
	var quoted []string
	for _, p := range parts {
		p = strings.TrimSpace(p)
		quoted = append(quoted, fmt.Sprintf("%q", p))
	}
	return strings.Join(quoted, ", ")
}

func scan(dir string) ([]discoveredAnnotation, []string) {
	// ... same scan logic as before but without the annotation collector
	// reusing the scan function from the existing code
	var result []discoveredAnnotation
	var structs []string
	seen := make(map[string]bool)

	fset := token.NewFileSet()
	pkgs, err := parser.ParseDir(fset, dir, func(info os.FileInfo) bool {
		return strings.HasSuffix(info.Name(), ".go") && !strings.HasSuffix(info.Name(), "_test.go")
	}, parser.ParseComments)
	if err != nil {
		return result, structs
	}

	for _, pkg := range pkgs {
		for _, file := range pkg.Files {
			for _, decl := range file.Decls {
				genDecl, ok := decl.(*ast.GenDecl)
				if !ok || genDecl.Tok != token.TYPE {
					continue
				}
				for _, spec := range genDecl.Specs {
					ts, ok := spec.(*ast.TypeSpec)
					if !ok {
						continue
					}
					if _, ok := ts.Type.(*ast.StructType); ok {
						n := ts.Name.Name
						if n == "" || n[0] < 'A' || n[0] > 'Z' {
							continue
						}
						if !seen[n] {
							structs = append(structs, n)
							seen[n] = true
						}
					}
				}
			}

			for _, decl := range file.Decls {
				fd, ok := decl.(*ast.FuncDecl)
				if !ok || fd.Recv == nil || fd.Doc == nil {
					continue
				}
				baseName := strings.TrimPrefix(receiverType(fd.Recv.List[0].Type), "*")
				if !seen[baseName] {
					continue
				}
				target := annotations.AnnotationTarget{StructName: baseName, MethodName: fd.Name.Name}
				for _, c := range fd.Doc.List {
					name, args, ok := annotations.Parse(c.Text)
					if !ok {
						continue
					}
					result = append(result, discoveredAnnotation{
						Name:    name,
						Target:  target,
						Args:    args,
						Context: "method",
					})
				}
			}
		}
	}
	return result, structs
}

func receiverType(expr ast.Expr) string {
	switch t := expr.(type) {
	case *ast.StarExpr:
		if ident, ok := t.X.(*ast.Ident); ok {
			return "*" + ident.Name
		}
	case *ast.Ident:
		return t.Name
	}
	return ""
}
