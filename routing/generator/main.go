func generateSubscribers(outDir, module string, anns []discoveredAnnotation) {
	hasSubs := false
	for _, a := range anns {
		if a.Name == "Subscribe" && a.Context == "method" && a.Args["event"] != "" {
			hasSubs = true
			break
		}
	}
	if !hasSubs {
		return
	}
	var buf bytes.Buffer
	buf.WriteString("// Code generated by gmcore; DO NOT EDIT.\n\n")
	buf.WriteString("package generated\n\n")
	buf.WriteString("import (\n")
	buf.WriteString("\t\"github.com/gmcorenet/framework/container\"\n")
	buf.WriteString("\t\"github.com/gmcorenet/framework/events\"\n")
	buf.WriteString(")\n\n")

	buf.WriteString("func RegisterGeneratedSubscribers(c *container.Container) {\n")
	for _, a := range anns {
		if a.Name != "Subscribe" || a.Context != "method" {
			continue
		}
		sid := annotations.TypeToServiceID(a.Target.StructName)
		event := a.Args["event"]
		if event == "" {
			continue
		}

		buf.WriteString(fmt.Sprintf("\tif svc, err := c.Get(%q); err == nil {\n", sid))
		buf.WriteString(fmt.Sprintf("\t\tif disp, ok := c.Get(\"dispatcher\"); ok {\n"))
		buf.WriteString(fmt.Sprintf("\t\t\tif bus, ok := disp.(events.EventDispatcher); ok {\n"))
		buf.WriteString(fmt.Sprintf("\t\t\t\tbus.Subscribe(%q, events.WrapListener(svc, %q))\n", event, a.Target.MethodName))
		buf.WriteString("\t\t\t}\n\t\t}\n\t}\n\n")
	}
	buf.WriteString("}\n")
	writeGo(outDir+"/subscribers_gen.go", buf.Bytes())
}

