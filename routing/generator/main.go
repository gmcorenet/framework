func generateSecurity(outDir, module string, anns []discoveredAnnotation) {
	var buf bytes.Buffer
	buf.WriteString("// Code generated by gmcore; DO NOT EDIT.\n\n")
	buf.WriteString("package generated\n\n")
	buf.WriteString("import \"github.com/gmcorenet/framework/container\"\n\n")
	buf.WriteString("func RegisterGeneratedSecurity(c *container.Container) {\n")
	for _, a := range anns {
		if a.Name != "Security" || a.Context != "method" {
			continue
		}
		sid := annotations.TypeToServiceID(a.Target.StructName)
		role := a.Args["role"]
		roles := a.Args["roles"]
		strategy := a.Args["strategy"]
		key := a.Args["key"]

		if role == "" && roles == "" && key == "" {
			continue
		}

		parts := []string{}
		if role != "" {
			parts = append(parts, fmt.Sprintf("\"role\":%q", role))
		}
		if roles != "" {
			parts = append(parts, fmt.Sprintf("\"roles\":[]string{%s}", formatRoles(roles)))
		}
		if strategy != "" {
			parts = append(parts, fmt.Sprintf("\"strategy\":%q", strategy))
		}
		if key != "" {
			parts = append(parts, fmt.Sprintf("\"key\":%q", key))
		}

		buf.WriteString(fmt.Sprintf("\tc.Set(\"security.%s.%s\", map[string]interface{}{%s})\n",
			sid, a.Target.MethodName, strings.Join(parts, ", ")))
	}
	buf.WriteString("}\n")

	buf.WriteString("\nfunc RegisterSecurityRouteMap(c *container.Container) {\n")
	buf.WriteString("\trouteMap := map[string]map[string]interface{}{\n")
	for _, a := range anns {
		if a.Name != "Route" || a.Context != "method" {
			continue
		}
		sid := annotations.TypeToServiceID(a.Target.StructName)
		buf.WriteString(fmt.Sprintf("\t\t%q: {\"controller\":%q,\"action\":%q},\n",
			a.Args["path"], sid, a.Target.MethodName))
	}
	buf.WriteString("\t}\n")
	buf.WriteString("\tc.Set(\"security.route_map\", routeMap)\n")
	buf.WriteString("}\n")

	writeGo(outDir+"/security_gen.go", buf.Bytes())
}

