type discoveredAnnotation struct {
	Name    string
	Target  annotations.AnnotationTarget
	Args    map[string]string
	Context string
}

func main() {
	sourceDir := "internal/controller"
	outDir := "internal/generated"
	module := "github.com/gmcorenet/skeleton"
	if len(os.Args) > 1 {
		sourceDir = os.Args[1]
	}
	if len(os.Args) > 2 {
		outDir = os.Args[2]
	}
	if len(os.Args) > 3 {
		module = os.Args[3]
	}

	anns, structs := scan(sourceDir)
	os.MkdirAll(outDir, 0755)

	generateRegister(outDir, module, structs)
	generateRoutes(outDir, module, anns)
	generateRateLimits(outDir, module, anns)
	generateSubscribers(outDir, module, anns)
	generateSecurity(outDir, module, anns)
	generateCaches(outDir, module, anns)
	generateValidators(outDir, module, anns)
	generateTemplates(outDir, module, anns)

	imports := readServicesYAML("config/services.yaml")
	if len(imports) > 0 {
		generateServices(outDir, module, imports)

		total := 0
		total += len(structs)
		total += len(imports)
		for _, a := range anns {
			switch a.Name {
			case "Route", "RateLimit", "Subscribe":
				total++
			}
		}
		fmt.Printf("Generated %d controller(s), %d service(s), %d annotation(s) in %s/\n",
			len(structs), len(imports), len(anns), outDir)
		return
	}

	routeCount, rateCount, subCount := countAnnotations(anns)
	fmt.Printf("Generated %d controller(s), %d route(s), %d ratelimit(s), %d subscriber(s) in %s/\n",
		len(structs), routeCount, rateCount, subCount, outDir)
}

func countAnnotations(anns []discoveredAnnotation) (routes, rates, subs int) {
	for _, a := range anns {
		switch a.Name {
		case "Route":
			routes++
		case "RateLimit":
			rates++
		case "Subscribe":
			subs++
		}
	}
	return
}

func generateRegister(outDir, module string, structs []string) {
	var buf bytes.Buffer
	buf.WriteString("// Code generated by gmcore; DO NOT EDIT.\n\n")
	buf.WriteString("package generated\n\n")
	buf.WriteString("import (\n")
	buf.WriteString("\t\"github.com/gmcorenet/framework/routing\"\n")
	buf.WriteString(fmt.Sprintf("\t_controller \"%s/internal/controller\"\n", module))
	buf.WriteString(")\n\n")
	buf.WriteString("func init() {\n")
	for _, s := range structs {
		buf.WriteString(fmt.Sprintf("\trouting.Register(func() interface{} { return &_controller.%s{} })\n", s))
	}
	buf.WriteString("}\n")
	writeGo(outDir+"/register_gen.go", buf.Bytes())
}

func generateRoutes(outDir, module string, anns []discoveredAnnotation) {
	var buf bytes.Buffer
	buf.WriteString("// Code generated by gmcore; DO NOT EDIT.\n\n")
	buf.WriteString("package generated\n\n")
	buf.WriteString("import (\n")
	buf.WriteString("\t\"github.com/gmcorenet/framework/container\"\n")
	buf.WriteString("\t\"github.com/gmcorenet/framework/router\"\n")
	buf.WriteString("\t\"github.com/gmcorenet/framework/routing\"\n")
	buf.WriteString(")\n\n")

	buf.WriteString("func RegisterGeneratedRoutes(r *router.Router, c *container.Container) []map[string]interface{} {\n")
	buf.WriteString("\tvar exposed []map[string]interface{}\n\n")

	hasRoutes := false
	for _, a := range anns {
		if a.Name != "Route" || a.Context != "method" {
			continue
		}
		hasRoutes = true
		sid := annotations.TypeToServiceID(a.Target.StructName)
		methodsRaw := a.Args["methods"]
		if methodsRaw == "" {
			methodsRaw = "GET"
		}
		methods := annotations.SplitList(methodsRaw)
		name := a.Args["name"]
		if name == "" {
			name = sid + "_" + strings.ToLower(a.Target.MethodName)
		}
		isPublic := a.Args["public"] == "true"

		buf.WriteString(fmt.Sprintf("\tif svc, err := c.Get(%q); err == nil {\n", sid))
		buf.WriteString(fmt.Sprintf("\t\troute, _ := r.Handle(%q, %s, routing.MakeHandler(svc, %q))\n",
			convertPath(a.Args["path"]), formatSlice(methods), a.Target.MethodName))
		buf.WriteString(fmt.Sprintf("\t\troute.Name = %q\n", name))
		if isPublic {
			buf.WriteString(fmt.Sprintf("\t\texposed = append(exposed, map[string]interface{}{\"name\":%q,\"path\":%q,\"methods\":%s})\n",
				name, a.Args["path"], formatSlice(methods)))
		}
		buf.WriteString("\t}\n\n")
	}

	buf.WriteString("\treturn exposed\n")
	buf.WriteString("}\n")

	if hasRoutes {
		writeGo(outDir+"/routes_gen.go", buf.Bytes())
	}
}

